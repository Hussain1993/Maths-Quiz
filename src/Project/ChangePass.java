    private void txtNewPassFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtNewPassFocusLost
        try{
            if (txtNewPass.getText().isEmpty())
            {
                throw new Exception ("Enter new password");
            }
            else
            {
                String patt = "^(?=.*\\d)(?=.*[a-z])(?=.*[A-Z]).{6,20}$";
                /**
                 * this is the same regular expression from RegisterUser
                 * for the password
                 */
                Pattern regPat = Pattern.compile(patt);
                Matcher matcher = regPat.matcher(txtNewPass.getText());
                if (matcher.find())
                {
                    lblNewPass.setForeground(Color.BLACK);
                }
                else
                {
                    lblNewPass.setForeground(Color.RED);
                }
                if (txtNewPass.getText().equals(txtOld.getText()))// check if the new password is the same as the old password
                {
                    throw new Exception ("You can't have the same password");
                }
            }
            lblMessage.setText("");
        }
        catch(Exception e){
            lblMessage.setForeground(Color.RED);
            lblMessage.setText(e.getMessage());
        }
    }//GEN-LAST:event_txtNewPassFocusLost

