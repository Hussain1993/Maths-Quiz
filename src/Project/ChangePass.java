public void change(){
    //this changes the password for the user
    String pass = txtNewPass.getText();
    String old = "";
     try{
         DatabaseConnect.init();
         Connection conn = DatabaseConnect.getConnection();
         Statement stmt = conn.createStatement();
         ResultSet result1 = stmt.executeQuery("SELECT PASSWORD FROM LOGIN WHERE FIRST_NAME = '"+first2+"'");
         if (result1.next() && result1!=null)
         {
             old = result1.getString("PASSWORD");
         }
         if (txtOld.getText().equals(old))//check if the entered old password is correct
         {
             stmt.executeUpdate("UPDATE LOGIN SET PASSWORD='"+pass+"'");//update password
             ResultSet result = stmt.executeQuery("SELECT * FROM LOGIN WHERE PASSWORD='"+pass+"'");//check that it has been updated
             if (result.next())
             {
                 this.dispose();
                 new MainMenu().setVisible(true);//display main menu if it has been updated
             }
             else
             {
                 lblMessage.setText("The password was not changed");
             }
         }
         else
         {
             lblMessage.setText("Old password wrong");
         }
         stmt.close();
         conn.close();
         result1.close();
     }
     catch(Exception ex){
         System.err.print("SQLException: ");
         System.err.println(ex.getMessage());
     }
}//end method
public boolean blankForm(){
    //check for a blank form
    boolean state = true;
    if (txtOld.getText().isEmpty())
    {
        state = false;
        lblOld.setForeground(Color.RED);
    }
    else
    {
        lblOld.setForeground(Color.BLACK);
    }
    if (txtNewPass.getText().isEmpty())
    {
        state = false;
        lblNewPass.setForeground(Color.RED);
    }
    else
    {
        valPass1();
        lblNewPass.setForeground(Color.BLACK);
    }
    if (txtNewPass2.getText().isEmpty())
    {
        state = false;
        lblNewPass2.setForeground(Color.RED);
    }
    else
    {
        valPass2();
        lblNewPass2.setForeground(Color.BLACK);
    }
    return state;
}//end method
public boolean valPass1(){
     //validate password 1
     boolean state = true;
     String patt = "^(?=.*\\d)(?=.*[a-z])(?=.*[A-Z]).{6,20}$";
     Pattern regPat = Pattern.compile(patt);
     Matcher matcher = regPat.matcher(txtNewPass.getText());
     if (matcher.find())
     {
        lblNewPass.setForeground(Color.BLACK);
     }
     else
     {
        lblNewPass.setForeground(Color.RED);
        state = false;
     }
     if (txtNewPass.getText().equals(txtOld.getText()))
     {
         lblNewPass.setForeground(Color.RED);
         state = false;
     }
     else
     {
         lblNewPass.setForeground(Color.BLACK);
     }
     return state;
}//end method
public boolean valPass2(){
    //check that the re-entered password match
     boolean state = true;
     if (!txtNewPass2.getText().equals(txtNewPass.getText()))
     {
         state = false;
         lblNewPass2.setForeground(Color.RED);
     }
     else
     {
         lblNewPass2.setForeground(Color.BLACK);
     }
     return state;
}//end method
