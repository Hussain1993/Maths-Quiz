    public void enter(){
        //this checks that the correct password has been entered to proceed
        try{
            
            String pass = txtPass.getText();
            DatabaseConnect.init();
            Connection conn = DatabaseConnect.getConnection();
            Statement stmt = conn.createStatement();
            ResultSet result = stmt.executeQuery("SELECT * FROM LOGIN WHERE PASSWORD='"+pass+"'");
            if (result.next())
            {
                //opens pupilDiary if the password is correct
                this.dispose();
                new pupilDiary().setVisible(true);
            }
            else
            {
                lblMessage.setText("Try Again");
            }
            stmt.close();
            conn.close();
            result.close();
        }
        catch(Exception ex){
            System.err.print("SQLException: ");
            System.err.println(ex.getMessage());
        }//end catch
    }//end catch
